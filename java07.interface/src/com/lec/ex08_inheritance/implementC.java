package com.lec.ex08_inheritance;

public class implementC implements C {

	@Override
	public void methodA() {
		System.out.println("InterfaceA.methodA()");
		
	}

	@Override
	public void methodB() {
		System.out.println("InterfaceB.methodB()");
		
	}

	@Override
	public void methodC() {
		System.out.println("InterfaceC.methodC()");
		
	}
	
	

}
